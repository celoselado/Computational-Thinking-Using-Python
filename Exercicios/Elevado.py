## Exericio 4= Escreva um algoritmo que lê dois números A e B e imprima o resultado de A elevado a B 

base = float(input("Digite qual é a base da potenciação: "))
expoente = float(input("Agora digite o valor do expoente: "))

potenciacao = base ** expoente

print ("A potencição da base",base," elevado ao expoente",expoente,"é igual a:",potenciacao)